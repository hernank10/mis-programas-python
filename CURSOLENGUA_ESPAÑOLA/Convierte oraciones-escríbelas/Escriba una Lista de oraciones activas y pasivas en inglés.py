# Función para practicar oraciones activas -> pasivas
def active_to_passive():
    for i in range(len(active_sentences)):
        print(f"Active sentence #{i+1}: {active_sentences[i]}")
        user_input = input("Now, write the passive form of this sentence: ").strip()
        
        if user_input == passive_sentences[i]:
            print("Correct!")
        else:
            print(f"Incorrect. The correct passive sentence is: {passive_sentences[i]}")
        print()

# Función para practicar oraciones pasivas -> activas
def passive_to_active():
    for i in range(len(passive_sentences)):
        print(f"Passive sentence #{i+1}: {passive_sentences[i]}")
        user_input = input("Now, write the active form of this sentence: ").strip()
        
        if user_input == active_sentences[i]:
            print("Correct!")
        else:
            print(f"Incorrect. The correct active sentence is: {active_sentences[i]}")
        print()

# Función para mostrar el menú y seleccionar opción
def menu():
    while True:
        print("Practice active and passive sentences.")
        print("1. Convert active sentences to passive")
        print("2. Convert passive sentences to active")
        print("3. Exit")
        
        choice = input("Please choose an option (1-3): ")
        
        if choice == "1":
            active_to_passive()
        elif choice == "2":
            passive_to_active()
        elif choice == "3":
            print("Exiting the program.")
            break
        else:
            print("Invalid option. Please choose again.")
        print()

# Ejecuta el programa
if __name__ == "__main__":
    menu()
