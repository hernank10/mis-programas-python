def activas_a_pasivas():
    for i in range(len(oraciones_activas)):
        print(f"Oración activa #{i+1}: {oraciones_activas[i]}")
        entrada_usuario = input("Ahora, escribe la forma pasiva de esta oración: ").strip()
        
        if entrada_usuario == oraciones_pasivas[i]:
            print("¡Correcto!")
        else:
            print(f"Incorrecto. La oración pasiva correcta es: {oraciones_pasivas[i]}")
        print()

