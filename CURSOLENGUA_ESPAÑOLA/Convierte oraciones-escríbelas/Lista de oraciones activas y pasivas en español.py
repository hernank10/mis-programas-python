# Función para mostrar el menú y seleccionar opción
def menu():
    while True:
        print("Practica oraciones en voz activa y pasiva.")
        print("1. Convertir oraciones activas a pasivas")
        print("2. Convertir oraciones pasivas a activas")
        print("3. Salir")
        
        opcion = input("Por favor, elige una opción (1-3): ")
        
        if opcion == "1":
            activas_a_pasivas()
        elif opcion == "2":
            pasivas_a_activas()
        elif opcion == "3":
            print("Saliendo del programa.")
            break
        else:
            print("Opción no válida. Por favor, elige de nuevo.")
        print()

# Ejecuta el programa
if __name__ == "__main__":
    menu()
