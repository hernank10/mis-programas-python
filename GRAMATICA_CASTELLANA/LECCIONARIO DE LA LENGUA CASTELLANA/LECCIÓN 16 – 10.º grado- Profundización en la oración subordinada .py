    print("\n🧠 Teoría avanzada:")
    print("- Funcionan como sustantivos: sujeto, CD, término de preposición.")
    print("- Introducidas por: que, si, pronombres interrogativos.")
    print("Ejemplo: El director exigió que entregáramos el informe.")

    print("\n📝 Ejercicio 1: Señala subordinada sustantiva y su función.")
    input("a) Me parece evidente que no vendrá. Subordinada y función: ")
    input("b) Aún no sabemos si aprobarán todos. Subordinada y función: ")
    input("c) Explícame qué te molesta. Subordinada y función: ")

    print("\n📝 Ejercicio 2: Redacta un párrafo con 2 subordinadas sustantivas.")
