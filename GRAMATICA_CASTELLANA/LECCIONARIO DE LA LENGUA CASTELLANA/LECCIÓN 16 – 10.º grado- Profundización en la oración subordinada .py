    print("¿Cuál es el núcleo verbal de la subordinada en: 'Estoy seguro de que aprobarás'?")
    print("a) Estoy   b) seguro   c) aprobarás   d) de")
    respuesta = input("Tu respuesta (a/b/c/d): ").lower()
    
    if respuesta == "c":
        print("✅ Correcto. 'aprobarás' es el núcleo verbal de la subordinada sustantiva.")
