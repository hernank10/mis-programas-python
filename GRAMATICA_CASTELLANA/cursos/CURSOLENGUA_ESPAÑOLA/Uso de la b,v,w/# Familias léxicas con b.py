# Familias léxicas con "b"

familia_b_1 = ["buscar", "buscador", "busca", "búsqueda"]
familia_b_2 = ["besar", "beso", "besuqueo", "besuque"]
familia_b_3 = ["bueno", "bondad", "bonito", "bonanza"]

# Familias léxicas con "v"

familia_v_1 = ["venir", "venida", "vendedor", "vendedor"]
familia_v_2 = ["volver", "vuelta", "volveré", "envoltorio"]
familia_v_3 = ["vida", "vivir", "vivo", "vivienda"]

# Imprimiendo las familias léxicas
print("Familias léxicas con 'b':")
for familia in [familia_b_1, familia_b_2, familia_b_3]:
  print(familia)

print("\nFamilias léxicas con 'v':")
for familia in [familia_v_1, familia_v_2, familia_v_3]:
  print(familia)
