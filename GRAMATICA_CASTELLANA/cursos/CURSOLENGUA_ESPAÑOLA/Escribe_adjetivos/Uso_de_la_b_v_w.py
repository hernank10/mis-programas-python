# Función para revisar las reglas
def revisar_reglas(reglas):
    for i, regla in enumerate(reglas, 1):
        print(f"Rule {i}: {regla}")
        input("Press Enter to continue...")
        print("Please write the rule you just read:")
        respuesta = input()
        if respuesta.strip() == regla:
            print("Correct!")
        else:
            print("Incorrect. The correct answer is:")
            print(regla)
        print()

