def ejercicio_asociacion():
    palabra = random.choice(list(easociacion.keys()))
    respuesta = input(f"\u041a\u0430\u043a\u043e\u0435 \u0441\u043b\u043e\u0432\u043e \u0441\u043e\u043e\u0442\u0432\u0435\u0442\u0441\u0442\u0432\u0443\u0435\u0442 \u0438\u0437\u043e\u0431\u0440\u0430\u0436\u0435\u043d\u0438\u044e? ({easociacion[palabra]}) ")
    if respuesta.lower() == palabra:
        print("\u0412\u0435\u0440\u043d\u043e! \u041c\u043e\u043b\u043e\u0434\u0435\u0446!")
    else:
        print(f"\u041d\u0435\u0432\u0435\u0440\u043d\u043e. \u041f\u0440\u0430\u0432\u0438\u043b\u044c\u043d\u044b\u0439 \u043e\u0442\u0432\u0435\u0442: {palabra}")

def ordenar_palabras():
    oracion = random.choice(ord_palabras)
    palabras = oracion.split()
    random.shuffle(palabras)
    respuesta = input(f"\u0423\u043f\u043e\u0440\u044f\u0434\u043e\u0447\u044c\u0442\u0435 \u044d\u0442\u0438 \u0441\u043b\u043e\u0432\u0430: {' '.join(palabras)} ")
    if respuesta.lower() == oracion.lower():
        print("\u0412\u0435\u0440\u043d\u043e! \u041c\u043e\u043b\u043e\u0434\u0435\u0446!")
    else:
        print(f"\u041d\u0435\u0432\u0435\u0440\u043d\u043e. \u041f\u0440\u0430\u0432\u0438\u043b\u044c\u043d\u044b\u0439 \u043e\u0442\u0432\u0435\u0442: {oracion}")

def rellenar_espacios():
    oracion = random.choice(relleno)
    respuesta = input(f"{oracion} ")
    if ("\u0441\u043e\u043b\u043d\u0446\u0435" in oracion and respuesta.lower() == "\u0441\u043e\u043b\u043d\u0446\u0435") or ("\u0434\u043e\u043c" in oracion and respuesta.lower() == "\u0434\u043e\u043c"):
        print("\u0412\u0435\u0440\u043d\u043e! \u041c\u043e\u043b\u043e\u0434\u0435\u0446!")
    else:
        print("\u041d\u0435\u0432\u0435\u0440\u043d\u043e. \u041f\u043e\u043f\u0440\u043e\u0431\u0443\u0439\u0442\u0435 \u0435\u0449\u0435 \u0440\u0430\u0437.")

