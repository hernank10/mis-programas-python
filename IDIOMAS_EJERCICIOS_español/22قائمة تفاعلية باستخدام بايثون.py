import random

def تحديد_المعنى():
    القاموس = {"correr": "المشي بسرعة", "brillante": "يعكس الضوء بقوة"}
    الكلمة, المعنى = random.choice(list(القاموس.items()))
    الخيارات = [المعنى, "إجابة خاطئة 1", "إجابة خاطئة 2"]
    random.shuffle(الخيارات)
    print(f"ما معنى الكلمة '{الكلمة}'؟")
    for i, الخيار in enumerate(الخيارات, 1):
        print(f"{i}. {الخيار}")
    الإجابة = int(input("اختر الإجابة الصحيحة: "))
    print("صحيح!" if الخيارات[الإجابة - 1] == المعنى else "خطأ")

def مرادفات_ومضادات():
    المرادفات = {"feliz": "alegre", "rápido": "veloz"}
    المضادات = {"feliz": "triste", "rápido": "lento"}
    الكلمة = random.choice(list(المرادفات.keys()))
    print(f"اكتب المرادف أو المضاد للكلمة '{الكلمة}': ")
    الإجابة = input().strip()
    if الإجابة in (المرادفات[الكلمة], المضادات[الكلمة]):
        print("صحيح!")
    else:
        print(f"خطأ. الإجابة الصحيحة: مرادف '{المرادفات[الكلمة]}'، مضاد '{المضادات[الكلمة]}'")

def ملء_الفراغ():
    الجمل = {"brillante": "اليوم الشمس مشرقة جدًا ______."}
    الكلمة, الجملة = random.choice(list(الجمل.items()))
    print(الجملة)
    الإجابة = input("أدخل الكلمة الصحيحة: ").strip()
    if الإجابة == الكلمة:
        print("صحيح!")
    else:
        print(f"خطأ، الإجابة الصحيحة: '{الكلمة}'")

def تصنيف_الكلمات():
    الفئات = {"científico": "تقني", "pintoresco": "أدبي"}
    الكلمة, الفئة = random.choice(list(الفئات.items()))
    print(f"إلى أي فئة تنتمي الكلمة '{الكلمة}'؟")
    الإجابة = input("الخيارات: تقني، عامي، أدبي: ").strip().lower()
    if الإجابة == الفئة:
        print("صحيح!")
    else:
        print(f"خطأ، الفئة الصحيحة: '{الفئة}'")

def وصف_الكلمة():
    الكلمة = "correr"
    print(f"اكتب تعريفًا للكلمة '{الكلمة}': ")
    input()
    print("شكرًا! التعريف الصحيح: المشي بسرعة.")

def القائمة():
    while True:
        print("\nقائمة تمارين الكلمات")
        print("1. تحديد معنى الكلمة")
        print("2. مرادفات ومضادات")
        print("3. ملء الفراغات")
        print("4. تصنيف الكلمات")
        print("5. وصف الكلمة")
        print("6. إنهاء")
        الاختيار = input("اختر خيارًا: ")
        if الاختيار == "1":
            تحديد_المعنى()
        elif الاختيار == "2":
            مرادفات_ومضادات()
        elif الاختيار == "3":
            ملء_الفراغ()
        elif الاختيار == "4":
            تصنيف_الكلمات()
        elif الاختيار == "5":
            وصف_الكلمة()
        elif الاختيار == "6":
            print("وداعًا!")
            break
        else:
            print("إدخال غير صالح. حاول مرة أخرى.")

القائمة()
