import random

def זיהוי_משמעות():
    מילון = {"correr": "לרוץ", "brillante": "מבריק"}
    מילה, משמעות = random.choice(list(מילון.items()))
    אפשרויות = [משמעות, "תשובה שגויה 1", "תשובה שגויה 2"]
    random.shuffle(אפשרויות)
    print(f"מהי משמעות המילה '{מילה}'?")
    for i, אפשרות in enumerate(אפשרויות, 1):
        print(f"{i}. {אפשרות}")
    תשובה = int(input("בחר את התשובה הנכונה: "))
    print("נכון!" if אפשרויות[תשובה - 1] == משמעות else "לא נכון")

def נרדפות_והפכים():
    נרדפות = {"feliz": "alegre", "rápido": "veloz"}
    הפכים = {"feliz": "triste", "rápido": "lento"}
    מילה = random.choice(list(נרדפות.keys()))
    print(f"כתוב נרדפת או הפך למילה '{מילה}': ")
    תשובה = input().strip()
    if תשובה in (נרדפות[מילה], הפכים[מילה]):
        print("נכון!")
    else:
        print(f"לא נכון. התשובה הנכונה: נרדפת '{נרדפות[מילה]}', הפך '{הפכים[מילה]}'")

def השלמת_משפט():
    משפטים = {"brillante": "השמש היום מאוד ______."}
    מילה, משפט = random.choice(list(משפטים.items()))
    print(משפט)
    תשובה = input("הכנס את המילה הנכונה: ").strip()
    if תשובה == מילה:
        print("נכון!")
    else:
        print(f"לא נכון, התשובה הנכונה היא: '{מילה}'")

def סיווג_מילים():
    קטגוריות = {"científico": "טכני", "pintoresco": "ספרותי"}
    מילה, קטגוריה = random.choice(list(קטגוריות.items()))
    print(f"לאיזו קטגוריה שייכת המילה '{מילה}'?")
    תשובה = input("אפשרויות: טכני, ספרותי, יום-יומי: ").strip().lower()
    if תשובה == קטגוריה:
        print("נכון!")
    else:
        print(f"לא נכון, הקטגוריה הנכונה היא: '{קטגוריה}'")

def תיאור_מילה():
    מילה = "correr"
    print(f"כתוב הגדרה למילה '{מילה}': ")
    input()
    print("תודה! ההגדרה הנכונה היא: לרוץ.")

def תפריט():
    while True:
        print("\nתפריט תרגול מילים")
        print("1. זיהוי משמעות המילה")
        print("2. נרדפות והפכים")
        print("3. השלמת משפטים")
        print("4. סיווג מילים")
        print("5. תיאור מילה")
        print("6. יציאה")
        בחירה = input("בחר אפשרות: ")
        if בחירה == "1":
            זיהוי_משמעות()
        elif בחירה == "2":
            נרדפות_והפכים()
        elif בחירה == "3":
            השלמת_משפט()
        elif בחירה == "4":
            סיווג_מילים()
        elif בחירה == "5":
            תיאור_מילה()
        elif בחירה == "6":
            print("להתראות!")
            break
        else:
            print("קלט לא חוקי. נסה שוב.")

תפריט()
